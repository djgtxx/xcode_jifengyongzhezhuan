function getErrStr(code)

	if errcode[code] ~= nil then
		return errcode[code]
	else
		return ""..code
	end
end

function printErrStr(msg)
	 print("msg type is "..type(msg))
   local code = msg.errcode
   local param = msg.param
   	if errcode[code] ~= nil then
	   	MainMenuLayer:getMainMenuLayer():hideUILayerByTag(UITagTable["ForcePKAnimLayer"].tag)
	   GameApi:showMessage(string.format(errcode[code], param))
	end
	return true
end
